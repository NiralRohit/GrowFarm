const Bill = require('../models/Bill.model');
const User = require('../models/User.model');
const PDFDocument = require('pdfkit');
const fs = require('fs');
const path = require('path');

/**
 * Generate unique bill number
 */
const generateBillNo = () => `APMC-${Date.now()}-${Math.floor(Math.random() * 1000)}`;

/**
 * Create a new bill and generate PDF
 */
exports.createBill = async (req, res) => {
    try {
        const { farmerId, cropName, quantity, rate, grade } = req.body;
        const traderId = req.user._id;

        // Calculate net payable
        const total = Number(quantity) * Number(rate);
        const billNo = generateBillNo();

        // Create Bill record
        const bill = new Bill({
            billNo,
            farmer: farmerId,
            trader: traderId,
            cropName,
            quantity,
            rate,
            netPayable: total,
            grade
        });

        // ─── PDF Generation ───
        const doc = new PDFDocument();
        const fileName = `${billNo}.pdf`;
        const filePath = path.join(__dirname, '..', 'uploads', 'bills', fileName);
        
        // Ensure directory exists
        const dir = path.dirname(filePath);
        if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });

        const stream = fs.createWriteStream(filePath);
        doc.pipe(stream);

        // PDF Content
        doc.fontSize(20).text('GrowFarm APMC Transaction Bill', { align: 'center' });
        doc.moveDown();
        doc.fontSize(12).text(`Bill No: ${billNo}`);
        doc.text(`Date: ${new Date().toLocaleDateString()}`);
        doc.moveDown();
        doc.text(`Trader ID: ${traderId}`);
        doc.text(`Farmer ID: ${farmerId}`);
        doc.moveDown();
        doc.text('------------------------------------------');
        doc.text(`Crop: ${cropName}`);
        doc.text(`Grade: ${grade}`);
        doc.text(`Quantity: ${quantity} Quintal`);
        doc.text(`Rate: ₹${rate} / Quintal`);
        doc.text('------------------------------------------');
        doc.fontSize(14).text(`Net Payable: ₹${total}`, { bold: true });
        doc.moveDown();
        doc.fontSize(10).text('Generated by GrowFarm Smart APMC System', { align: 'center', color: 'grey' });

        doc.end();

        // Wait for stream to finish
        stream.on('finish', async () => {
            bill.pdfUrl = `/uploads/bills/${fileName}`;
            await bill.save();
            res.status(201).json({ message: "Bill generated successfully", bill });
        });
    } catch (error) {
        res.status(500).json({ message: error.message });
    }
};

/**
 * Get bills for a specific farmer
 */
exports.getFarmerBills = async (req, res) => {
    try {
        const bills = await Bill.find({ farmer: req.user._id }).populate('trader', 'fullName phone');
        res.status(200).json(bills);
    } catch (error) {
        res.status(500).json({ message: error.message });
    }
};

